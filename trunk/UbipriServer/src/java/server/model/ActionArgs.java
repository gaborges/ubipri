package server.model;

/**
 *
 * @author Estudo
 */
public class ActionArgs {
    private int id;
    private String label ;
    private String value;

    public ActionArgs() {
        this.label = null;
    }

    public ActionArgs(int id, String label, String value) {
        this.id = id;
        this.label = label;
        this.value = value;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getLabel() {
        return label;
    }

    public void setLabel(String label) {
        this.label = label;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }
 
}
